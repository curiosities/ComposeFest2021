/*
modifiers allow you to customize the behavior of a composable

Order matters
order matters when chaining modifiers as they're applied to the composable they modify from earlier to later,
meaning that the measurement and layout of the modifiers on the left will affect the modifier on the right.
The final size of the composable depends on all modifiers passed as a parameter.

Layout direction
You can change the layout direction of a composable using the LayoutDirection ambient.
layoutDirection is part of the LayoutScope of the layout modifier or Layout composable. 
When using layoutDirection, place composables using place as unlike the placeRelative method, 
it won't automatically mirror the position in right-to-left context.

 */

// How to create a modifier
@Stable
fun Modifier.padding(all: Dp) =
    this.then(
        PaddingModifier(
            start = all,
            top = all,
            end = all,
            bottom = all,
            rtlAware = true
        )
    )

// Implementation detail
private class PaddingModifier(
    val start: Dp = 0.dp,
    val top: Dp = 0.dp,
    val end: Dp = 0.dp,
    val bottom: Dp = 0.dp,
    val rtlAware: Boolean,
) : LayoutModifier {

    override fun MeasureScope.measure(
        measurable: Measurable,
        constraints: Constraints,
    ): MeasureResult {

        val horizontal = start.roundToPx() + end.roundToPx()
        val vertical = top.roundToPx() + bottom.roundToPx()

        val placeable = measurable.measure(constraints.offset(-horizontal, -vertical))

        val width = constraints.constrainWidth(placeable.width + horizontal)
        val height = constraints.constrainHeight(placeable.height + vertical)
        return layout(width, height) {
            if (rtlAware) {
                placeable.placeRelative(start.roundToPx(), top.roundToPx())
            } else {
                placeable.place(start.roundToPx(), top.roundToPx())
            }
        }
    }
}

